package com.boe.posture.domain;



public class CoordinateBO {

    private double y;
    private double x;
    private double score;
    public void setY(double y) {
         this.y = y;
     }
     public double getY() {
         return y;
     }

    public void setX(double x) {
         this.x = x;
     }
     public double getX() {
         return x;
     }

    public void setScore(double score) {
         this.score = score;
     }
     public double getScore() {
         return score;
     }

}